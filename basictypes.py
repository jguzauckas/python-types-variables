#This program creates a variable of each of the four basic types.

#Integer
a = 3 #No decimal point makes it an integer (int)
print("The type of", a, "is", type(a)) #This should return int

#Float
b = 2.4 #The decimal point makes it a float (float)
print("The type of", b, "is", type(b)) #This should return float

#Boolean
c = True #The keywords True or False make it a boolean (bool)
print("The type of", c, "is", type(c)) #This should return bool

#String
d = "Hello" #Single or double quotes makes it a String (str)
print("The type of", d, "is", type(d)) #This should return str